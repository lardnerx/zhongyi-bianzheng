            <tr>
              <td><strong>先天之精</strong></td>
              <td>父母遗传</td>
              <td>出生时存折里的本金</td>
              <td>与生俱来，不可再生，用一点少一点。这就是为什么说"禀赋"决定了一个人的基础生命力</td>
            </tr>
