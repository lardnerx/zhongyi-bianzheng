        {/* ========== sec-m1-15: 层次病位总览 ========== */}
        <section id="sec-m1-15">
          <h2>十六、层次病位总览——卫气营血四道防线</h2>
          <p>
            前面我们讲了五脏六腑——这是身体里的"房间"。现在我们要学习的是另一种划分病位的方法——<strong>层次病位</strong>。
          </p>
          <p>
            如果说五脏六腑是横向划分（不同的房间），那么层次病位就是<strong>纵向划分</strong>（从外到内的深度）。中医把疾病入侵人体的深度分为四个层次：<strong>卫分 → 气分 → 营分 → 血分</strong>。这就像一座房子的<strong>四道防线</strong>：
          </p>
          <ul>
            <li><strong>卫分</strong>——房子的围墙和篱笆（最外层），看门狗在这里巡逻</li>
            <li><strong>气分</strong>——房子的客厅和厨房（第二层），日常活动的主战场</li>
            <li><strong>营分</strong>——房子的卧室（第三层），是休息和养精蓄锐的地方</li>
            <li><strong>血分</strong>——房子的保险柜（最里层），藏着家里最珍贵的东西</li>
          </ul>
          <p>
            病邪入侵的过程，通常是从外到里、由浅入深：卫分 → 气分 → 营分 → 血分。层次越深，病情越重。这种辨证方法叫做<strong>"卫气营血辨证"</strong>，是清代著名医家叶天士创立的，专门用于外感温热病的辨证。
          </p>
        </section>

        {/* ========== sec-m1-16: 卫分 ========== */}
        <section id="sec-m1-16">
          <h2>十七、卫分——篱笆和看门狗（最外层防线）</h2>

          <h3>什么是卫分？</h3>
          <p>
            <strong>卫分</strong>是身体最外层的防线，包括<strong>皮肤、毛孔、呼吸道黏膜</strong>等。"卫"就是保卫的意思，<strong>卫气</strong>就是身体表面巡逻的"保安队伍"，相当于房子周围的<strong>篱笆和看门狗</strong>。
          </p>

          <h3>卫分的功能</h3>
          <ul>
            <li><strong>抵御外邪：</strong>卫气在体表巡逻，阻挡病邪入侵。看门狗叫一声，敌人就不敢靠近。</li>
            <li><strong>调节体温：</strong>卫气控制毛孔的开合，调节体温。热了就出汗散热，冷了就收缩毛孔保温。</li>
            <li><strong>温养皮肤：</strong>卫气滋养皮肤和汗毛，让皮肤保持温暖和滋润。</li>
          </ul>

          <h3>卫分证的特点</h3>
          <p>
            当病邪侵犯卫分时，就是最常见的<strong>感冒初期</strong>。症状包括：
          </p>
          <ul>
            <li><strong>怕冷（恶寒）：</strong>看门狗被敌人打晕了，篱笆破了个洞，冷风直往里灌。</li>
            <li><strong>发热：</strong>身体在调动能量对抗病邪。</li>
            <li><strong>无汗或少汗：</strong>毛孔被病邪"锁住"了。</li>
            <li><strong>头痛、身痛：</strong>卫气被阻滞，气血运行不畅。</li>
            <li><strong>舌苔薄白、脉浮：</strong>病还在表层。</li>
          </ul>

          <h3>治疗方法：发汗解表</h3>
          <p>
            卫分证的治疗原则是<strong>"发汗解表"</strong>。通俗地说就是把篱笆修好，让看门狗恢复战斗力。最常见的方子就是<strong>麻黄汤</strong>和<strong>桂枝汤</strong>，用发汗的方法把病邪从体表赶出去。感冒了喝碗热姜汤、盖被子出出汗，就是这个道理。
          </p>
          <p>
            卫分证是病位最浅、最容易治疗的阶段。如果在这个阶段正确处理，病情就不会向内发展。这就是为什么中医强调"感冒要早治"——在篱笆阶段就把敌人赶走，别让它进到客厅和卧室。
          </p>
        </section>

        {/* ========== sec-m1-17: 气分 ========== */}
        <section id="sec-m1-17">
          <h2>十八、气分——客厅和厨房（主战场）</h2>

          <h3>什么是气分？</h3>
          <p>
            <strong>气分</strong>是病邪突破了卫分防线后，进入的第二层。气分涉及的是<strong>脏腑的功能活动层面</strong>，包括肺、胃、肠、胆等脏腑。如果卫分是房子的外墙和篱笆，气分就是<strong>客厅和厨房</strong>——家人日常活动的核心区域。
          </p>

          <h3>气分证的特点</h3>
          <p>
            病邪进入气分后，人体的正气（免疫力）会全力抵抗，双方展开激烈战斗。这个"战场"就是气分。症状比卫分更重、更明显：
          </p>
          <ul>
            <li><strong>大热（高热）：</strong>正气和邪气激烈交战，产生大量热量。卫分是"发热"，气分是"大热"——温度更高。</li>
            <li><strong>大渴：</strong>高热消耗了大量津液，需要不断喝水。</li>
            <li><strong>大汗：</strong>身体通过出汗来散热。</li>
            <li><strong>脉洪大：</strong>气血运行激烈，脉搏宽大有力。</li>
            <li><strong>舌苔黄：</strong>热象明显。</li>
          </ul>
          <p>
            根据病邪侵犯的具体脏腑不同，气分证还有一些特定的表现：
          </p>
          <ul>
            <li><strong>肺热：</strong>咳嗽、气喘、黄痰——像厨房里着火了，浓烟滚滚。</li>
            <li><strong>胃热：</strong>口渴、口臭、牙龈肿痛——像灶台上的火太大，锅都烧干了。</li>
            <li><strong>肠热：</strong>便秘、腹痛——像下水道被热"烤干"了，排泄困难。</li>
          </ul>

          <h3>治疗方法：清气分热</h3>
          <p>
            气分证的治疗原则是<strong>"清气分热"</strong>——用寒凉的药物把气分的热邪清出去。代表方剂是<strong>白虎汤</strong>，就像在熊熊燃烧的客厅里泼上一盆冷水，把火势扑灭。
          </p>
          <p>
            气分证是卫气营血辨证中<strong>最常见的阶段</strong>，大多数急性发热性疾病都在这个阶段被治愈。如果气分证处理得当，病情就会好转；如果处理不当或正气不足，病邪就会继续深入。
          </p>
        </section>

        {/* ========== sec-m1-18: 营分 ========== */}
        <section id="sec-m1-18">
          <h2>十九、营分——卧室（热扰心神）</h2>

          <h3>什么是营分？</h3>
          <p>
            <strong>营分</strong>是比气分更深的层次。"营"是营养、经营的意思。营分涉及的是<strong>血液的前身和心神层面</strong>。如果说气分是房子的客厅和厨房，营分就是<strong>卧室</strong>——这是人休息、睡觉、养精蓄锐的地方。
          </p>

          <h3>营分证的特点</h3>
          <p>
            营分证最核心的特征是<strong>心神被扰</strong>。邪热侵入营分，就像盗贼闯进了卧室，人在床上睡不安稳。症状包括：
          </p>
          <ul>
            <li><strong>身热夜甚：</strong>晚上发热更重，因为晚上阴气盛、阳气入阴分，热邪在营分被激发。</li>
            <li><strong>心烦失眠：</strong>热扰心神，让人烦躁不安、辗转难眠——就像卧室里太热了，根本睡不着。</li>
            <li><strong>谵语：</strong>严重时神志不清、说胡话——卧室的"主人"（心神）被热邪骚扰得神志错乱。</li>
            <li><strong>斑疹隐隐：</strong>皮肤可能出现隐隐约约的红点或斑块——热邪已经接触到血分了。</li>
            <li><strong>舌质红绛：</strong>舌头颜色深红，因为热已入营（接近血）。</li>
            <li><strong>脉细数：</strong>脉搏细而快，说明阴液已伤。</li>
          </ul>

          <h3>治疗方法：清营透热</h3>
          <p>
            营分证的治疗原则是<strong>"清营透热"</strong>——把营分的热邪"清"出去，同时把热邪"透"到气分来排出体外。代表方剂是<strong>清营汤</strong>。
          </p>
          <p>
            治疗营分证的思路很巧妙——<strong>"透热转气"</strong>，意思是不直接和营分的热邪硬碰硬（因为营分已经很深了，直接清热效果不好），而是先把热邪引导到气分（浅一层），再从气分清除出去。就像卧室里着火了，先打开窗户（透热）把浓烟排出去，再灭火。
          </p>
          <p>
            营分证已经到了<strong>比较危重的阶段</strong>，但还不是最深。如果不及时治疗，病邪就会进入最后也是最深的一层——血分。
          </p>
        </section>

        {/* ========== sec-m1-19: 血分 ========== */}
        <section id="sec-m1-19">
          <h2>二十、血分——保险柜（最深层）</h2>

          <h3>什么是血分？</h3>
          <p>
            <strong>血分</strong>是卫气营血辨证中最深的一层。"血"在这里不仅指血液，还包括<strong>肾精和骨髓</strong>等人体最根本的物质。如果卫分是篱笆、气分是客厅、营分是卧室，血分就是房子的<strong>保险柜</strong>——藏着家里最珍贵的东西。一旦病邪攻破了保险柜，就是最危险的情况。
          </p>

          <h3>血分证的特点</h3>
          <p>
            血分证是温热病的<strong>极期和后期</strong>，病情最危重。症状可以概括为"<strong>耗血、动血、伤阴、动风</strong>"：
          </p>
          <ul>
            <li><strong>耗血伤阴：</strong>高热持续不退，体内的津液和血液被大量消耗。表现为身体消瘦、皮肤干燥、口干舌燥、舌质深红如镜面（镜面舌）。就像保险柜里的"存折"被烧掉了——体内最宝贵的精血被大量消耗。</li>
            <li><strong>动血出血：</strong>热邪灼伤血络，导致各种出血——吐血、鼻血、便血、尿血、皮下瘀斑。就像房子的总水管爆了，到处漏水。</li>
            <li><strong>热极生风：</strong>高烧到一定程度，出现抽搐、角弓反张（身体向后弓起）、牙关紧闭。这是因为热邪耗伤了肝血、灼伤了筋脉——就像电线烧断了，机器开始乱抖。</li>
            <li><strong>神志深度障碍：</strong>昏迷、狂躁不安——保险柜里的"核心机密"（心神）被彻底扰乱了。</li>
          </ul>

          <h3>治疗方法：凉血散血</h3>
          <p>
            血分证的治疗原则是<strong>"凉血散血"</strong>——用大剂量的寒凉药物来"凉血"，同时用活血药物来"散血"。代表方剂是<strong>犀角地黄汤</strong>。
          </p>
          <ul>
            <li><strong>凉血：</strong>直接给血液"降温"，清除血中的热毒。</li>
            <li><strong>散血：</strong>防止血液被热邪灼伤后形成瘀血，疏通血管。</li>
          </ul>
          <p>
            血分证是卫气营血辨证的<strong>最深层次</strong>，也是病情最危重的阶段。但如果治疗得当，仍然有逆转的机会。而且中医认为"<strong>入血就恐耗血动血，直须凉血散血</strong>"——到了血分，不要犹豫，果断使用凉血散血的方法。
          </p>
        </section>

